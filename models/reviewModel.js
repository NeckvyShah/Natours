// calculating avg rating and total no of reviews each time a new review is added to a tour

//for this writing a static method on schema
reviewSchema.statics.calcAverageRatings = async function (tourId) {
  const stats = await this.aggregate([
    {
      $match: { tour: tourId },
    },
    {
      $group: {
        _id: '$tour',
        nRating: { $sum: 1 },
        avgRating: { $avg: '$rating' },
      },
    },
  ]);
  console.log(stats);
};

reviewSchema.pre('save', function (next) {
  // this- current doc and consturctor- is the model who created that doc
  this.constructor.calcAverageRatings(this.tour);
  next();
});

